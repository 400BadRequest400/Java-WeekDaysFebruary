package coreJava;

public class DataTypes {
  
	public static void main (String [] args) {
		
		String firstname = "jeff";
		String lastname = "slimane";
		
		System.out.println(firstname+" "+lastname);
		
		//Byte - 8 bit memory location
		byte number = -128;
		byte number2 = 127;
		
		//short - 16 bit
		short x = -32_768;
		short y = 32_767;
		
		//int - 32 bit
		int a = -2_147_483_648;
		int b = 2_147_483_647;
		
		
		// long - 64 bit
		long z = 3476594865645646l;
		
		//double -bdecimal 64 bit- d
		
		double f = 1.33d;
		
		//float 32 bit- f
		float g = 1.222f;
		
		//char - character - unicode - 16 bit
		char h = '\u00e5';
		System.out.println(h);
		
		// boolean - true or false
		boolean s = false;
		System.out.println(s);
		
		
		
		

	}
	
}
